    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
    try (BufferedReader br = new BufferedReader(new FileReader("List.txt"))) {
     listModel.clear(); // Clearing any previous elements from listModel
        int val = Integer.parseInt(br.readLine()); // Reading the number of lines to be read
        for (int i = 0; i < val; i++) {
            String ss = br.readLine(); // Reading a line of text from the file
            listModel.addElement(ss); // Adding the line to the listModel
    }
} catch (IOException | NumberFormatException e) {
    System.out.println("Error reading from file: " + e.getMessage()); // Handling file reading or number conversion errors
}

    }//GEN-LAST:event_jButton4ActionPerformed

